    /**
     * Sets the forms features depending on the current users level of access
     */
    public void setFeatures() {
        String[] currentUserDetails = new String[11]; //Stores current users details as an array
        currentUserDetails = current.getCurrentUserDetails();

        welcomeLbl.setText("Welcome, " + currentUserDetails[3] + " " + currentUserDetails[4] + "!");

        if (loa.equals("worker")) { //If user is a worker...
            currencyLbl.setVisible(false);
            currencyBox.setVisible(false);
            currencyBox.setEnabled(false);
            middleBtn.setVisible(false);
            middleBtn.setEnabled(false);
        }

        if (loa.equals("manager")) { //If user is a manager...
            currencyLbl.setVisible(false);
            currencyBox.setVisible(false);
            currencyBox.setEnabled(false);
            middleBtn.setText("Create Accounts");
        }

    }

    /**
     * Collects the 3 closest upcoming events from the events file
     * into a 2D array
     * 
     * @return 2D array containing the 3 closest events
     */
    public String[][] getClosestEvents() {
        int numRecords = Search.countRecords("events.txt"); //Stores number of records currently in events file
        String[][] allEvents = new String[numRecords][8]; //Stores all event records in events file as a 2D array
        String[][] closestEvents = new String[3][4]; //Stores the 3 closest events as a 2D array

        Sort sort = new Sort(); //New Sort object created

        allEvents = search.getAllEvents(numRecords);
        sort.eventsToAscendingOrder(allEvents, numRecords, 8, 4); //allEvents sorted to ascending order of date

        for (int i = 0; i < 3; i++) { //For 3 top events in allEvents[] (3 closest events)
            closestEvents[i][0] = allEvents[i][1];
            closestEvents[i][1] = allEvents[i][4];
            closestEvents[i][2] = allEvents[i][2];
            closestEvents[i][3] = allEvents[i][0];
        }

        return closestEvents;
    }

