    private void finalizeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizeBtnActionPerformed
        //Stores number of tickets a customer is currently booking...
        int numTickets = booking.getNumTickets();

        //Stores currently selected seats as a comma separated String
        String seats = booking.getCurrentBookingSeatsString();
        //Stores currently selected seats as an array
        String[] allSeats = seats.split(",");
        //Stores number of seats currently selected
        int numSeatsSelected = allSeats.length;

        //If customer has selected a seat for every ticket they are booking...
        if (!(seats.equals("")) && (numSeatsSelected == numTickets)) {
            bookingTimer.cancel();
            seatingPlanClock.stopClock();

            //Sets secondsLeft in BookingTimer class to how long customer has left to book
            secondsLeft.setSecondsLeft(timeLeft);

            //If form being used to book tickets...
            if (currentForm.equals("Booking Seating Plan")) {
                current.setCurrentForm("Booking View 2"); //Sets currentForm in Current class to "Booking View 2"

                EventBookingUI eventBookingForm = new EventBookingUI(); //New EventBookingUI object created
                eventBookingForm.setVisible(true);
            }

            //If form being used to edit basket item...
            if (currentForm.equals("Edit Booking Seating Plan")) {
                current.setCurrentForm("Edit Booking 2");

                EventBookingUI editBooking2Form = new EventBookingUI();
                editBooking2Form.setVisible(true);
            }

            dispose(); //Current form disposed of
        } else { //If user has not selected a seat for every ticket they are booking...
            JOptionPane.showMessageDialog(null, "Please select all " + numTickets + " of your seats.", "Seats Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_finalizeBtnActionPerformed

