    /**
     * Takes a user to the event they selected out of the 3 closest events displayed
     * 
     * @param eventID primary key of event selected by user
     */
    public void goTo(String eventID) {
        int numRecords = search.countRecords("events.txt"); //Stores number of records currently in events file
        String chosenEvent[] = search.eventBinarySearch(eventID, numRecords); //Stores details of event as an array
        int index; //Stores index of users chosen currency

        if (chosenEvent[0] != null) { //If array isnt empty (event found)...

            if (loa.equals("customer")) { //If user is a customer...
                index = currencyBox.getSelectedIndex();
                currency.setCurrencyIndex(index); //Sets currencyIndex in Currency class to index
            }

            current.setCurrentEvent(chosenEvent); //Sets currentEvent in Current class to chosenEvent
            current.setCurrentForm("Closest Event Info"); //Sets currentForm in Current class to "Closest Event Info"
            
            menuClock.stopClock();
            EventInfoUI eventInfoForm = new EventInfoUI(); //New EventInfoUI object created
            eventInfoForm.setVisible(true);
            dispose(); //Current form disposed of
            
        } else { //If array is empty (event not found)...
            JOptionPane.showMessageDialog(null, "Sorry, this event could not be found.", "Error!", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    /**
     * Sets a users chosen currency
     */
    public void setCurrency() {
        int index; //Stores index of the chosen currency
        index = currencyBox.getSelectedIndex();
        currency.setCurrencyIndex(index); //Sets currencyIndex in Currency class to index
    }
    
