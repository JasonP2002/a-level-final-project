    /**
     * Sets form features depending on forms current purpose
     */
    public void setFeatures() {
        //Stores current events title
        String currentEventTitle = current.getCurrentEventTitle();
        int numTickets; //Stores number of tickets a customer is currently booking

        //If form being used to book tickets or edit a basket item...
        if ((currentForm.equals("Booking Seating Plan")) || (currentForm.equals("Edit Booking Seating Plan"))) {
            //Initializes first frame of booking timer
            minutes = String.valueOf(timeLeft / 60);
            seconds = String.valueOf(timeLeft % 60);
            
            if (seconds.length() == 1) { //If seconds in single digits...
                seconds = 0 + seconds;
            }

            timerLbl.setText("Time Left: " + minutes + ":" + seconds);

            numTickets = booking.getNumTickets();
            eventTitleLbl.setText(currentEventTitle + " - " + numTickets + " Tickets");
        }

        //If form being used to view booked seats...
        if (currentForm.equals("View Seating Plan")) {
            titleLbl.setText("Customer Bookings");
            eventTitleLbl.setText(currentEventTitle);
            timerLbl.setText("");
            backBtn.setText("View Bookings");
            seatsLbl.setText("");
            finalizeBtn.setEnabled(false);
            finalizeBtn.setVisible(false);
        }
    }

