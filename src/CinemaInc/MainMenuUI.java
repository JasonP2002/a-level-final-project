    private void quickSearchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_quickSearchBtnActionPerformed
        //Stores users inputted event title, date and time
        String eventTitle = eventTitleTxt.getText();
        String eventDate = eventDateTxt.getText();
        String eventTime = eventTimeTxt.getText();

        boolean searchSuccess = search.quickSearch(eventTitle, eventDate, eventTime);

        if (searchSuccess == true) { //If event is found...

            if (loa.equals("customer")) { //If user is a customer...
                setCurrency(); //Set chosen currency
            }

            current.setCurrentForm("Event Info"); //Sets currentForm in Current class to "Event Info"

            menuClock.stopClock();
            EventInfoUI eventInfo = new EventInfoUI(); //New EventInfoUI object created
            eventInfo.setVisible(true);
            dispose(); //Current form disposed of

        } else { //If event not found...
            JOptionPane.showMessageDialog(null, "This event could not be found. Please make sure title, date and time are correct.", "Event Not Found", JOptionPane.ERROR_MESSAGE);
            eventTitleTxt.setText("");
            eventDateTxt.setText("");
            eventTimeTxt.setText("");
        }
    }//GEN-LAST:event_quickSearchBtnActionPerformed

